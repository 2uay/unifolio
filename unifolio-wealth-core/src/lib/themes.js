export const DEFAULT_THEME = 'redblackwhiteaccent';

export function applyTheme(themeId) {
  const theme = themes[themeId] || themes[DEFAULT_THEME];
  const root = document.documentElement;
  Object.entries(theme.colors).forEach(([key, value]) => {
    root.style.setProperty(key, value);
  });
}

export function getThemeById(themeId) {
  return themes[themeId] || themes[DEFAULT_THEME];
}

export function getAllThemes() {
  return Object.entries(themes).map(([id, theme]) => ({ id, ...theme }));
}

// Returns the chart color palette for the currently active theme
export function getChartColors(themeId) {
  const theme = themes[themeId] || themes[DEFAULT_THEME];
  return theme.chartColors;
}

// Convert hex color to HSL
export function hexToHSL(hex) {
  const r = parseInt(hex.slice(1, 3), 16) / 255;
  const g = parseInt(hex.slice(3, 5), 16) / 255;
  const b = parseInt(hex.slice(5, 7), 16) / 255;
  const max = Math.max(r, g, b);
  const min = Math.min(r, g, b);
  let h, s, l = (max + min) / 2;
  if (max === min) {
    h = s = 0;
  } else {
    const d = max - min;
    s = l > 0.5 ? d / (2 - max - min) : d / (max + min);
    switch (max) {
      case r: h = (g - b) / d + (g < b ? 6 : 0); break;
      case g: h = (b - r) / d + 2; break;
      case b: h = (r - g) / d + 4; break;
    }
    h /= 6;
  }
  return {
    h: Math.round(h * 360),
    s: Math.round(s * 100),
    l: Math.round(l * 100),
  };
}

// Convert HSL to HSL string format used in CSS
export function hslToString(h, s, l) {
  return `${h} ${s}% ${l}%`;
}

